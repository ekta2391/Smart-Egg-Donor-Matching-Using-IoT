    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Organization organization = (Organization) organizationPatientJComboBox.getSelectedItem();
        String name = fNameTxtField.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(null,"Please enter a name","Warning",JOptionPane.WARNING_MESSAGE);
        }
        
            
        else{  
    
        Employee employee=null;
        
        if(organization instanceof DonorOrganization){
            employee=new Donor();
            
        }
        employee.setName(name);
        organization.getEmployeeDirectory().createEmployee(employee);
        //System.out.println("The donor is"+employee);
        JOptionPane.showMessageDialog(null, "The Donor has been created");
        populateTable();
        }
        
    }//GEN-LAST:event_addJButtonActionPerformed

