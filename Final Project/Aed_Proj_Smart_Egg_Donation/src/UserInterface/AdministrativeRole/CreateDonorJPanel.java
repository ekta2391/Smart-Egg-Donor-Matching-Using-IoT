    private void populateOrgPatientComboBox() {
         organizationPatientJComboBox.removeAllItems();
        
        for (Organization organization : directory.getOrganizationList()){
            if(organization instanceof DonorOrganization){
              organizationPatientJComboBox.addItem(organization);  
            }
            
        }

    }
 private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) donorJTable.getModel();
        
        model.setRowCount(0);
        for(Organization org : directory.getOrganizationList()){
            if(org instanceof DonorOrganization){
                for (Employee employee : org.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[3];
            row[0] = employee.getId();
            row[1] = employee.getName();
           
            model.addRow(row); 
            }
        }
             
          
        }
    }


